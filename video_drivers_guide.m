#!/bin/bash

##########################################################################################
## 🖥️ ИНСТРУКЦИЯ ПО УСТАНОВКЕ ВИДЕОДРАЙВЕРОВ В ARCH LINUX ДЛЯ ПРАКИЧЕСКОГО ПРИМЕНЕНИЯ   ##
##########################################################################################
#
# ℹ️ Назначение: Руководство по установке графических драйверов
#              для установки в Arch Linux.
# 💡 Включает: Варианты для Intel, AMD, NVIDIA (включая проприетарные и nouveau),
#              настройку для Wayland, гибридной графики (switcherooctl),
#              проверочные утилиты (glmark2, vkcube, nvidia-smi и др.),
#              и опциональные настройки (TLP).
# ❗ Важно: Выполняется после основного макета, в chroot или в новой системе.
# 🔒 Не запускайте как скрипт! Копируйте и вставляйте блоками вручную.

# Структура:
#   0. Подготовка и определение видеокарты
#   1. Выбор варианта установки (Intel / AMD / NVIDIA nouveau / NVIDIA Proprietary)
#   2. Дополнительные пакеты (Vulkan ICD, VA-API)
#   3. Настройка модулей и initramfs (только для NVIDIA Proprietary)
#   4. Опциональные настройки для ноутбуков (TLP)
#   5. Возврат к основному макету
#   6. Проверка работы после перезагрузки
#   7. Использование switcherooctl в пользовательском сеансе

# 💡 Примечание: Это руководство является продолжением БЛОКА 12 из основного макета.
#                Выполняйте его строго после завершения БЛОКА 11.
##################################################################



######################################################
## 🔍 БЛОК 0: ПОДГОТОВКА И ОПРЕДЕЛЕНИЕ ВИДЕОКАРТЫ   ##
######################################################
#
# ℹ️ Зачем: Точно определить модель и производителя GPU.
# ❗ Важно: От этого зависит выбор драйвера и его версии.
# 💡 Используйте: `lspci`, `inxi`, сайт NVIDIA.

# Выполните в chroot или в Live-среде:
lspci -nn | grep -i 'vga\|3d\|display'
# ИЛИ (если inxi установлен):
inxi -G

# 📌 Запишите:
# - Производителя: Intel / AMD / NVIDIA
# - Модель карты (для NVIDIA — уточните поколение: Fermi, Kepler, Maxwell, Pascal, Turing, Ampere, Ada Lovelace и т.д.)
# - Проверьте совместимость на: https://www.nvidia.com/drivers/

# 💡 ВАЖНО О ПОКОЛЕНИЯХ NVIDIA:
# - Современные поколения (Pascal, Turing, Ampere, Ada Lovelace): Поддерживаются текущими Production Branch драйверами (`nvidia`, `nvidia-open-lts`, `nvidia-open-dkms`).
# - Старые поколения (Tesla, Fermi, Kepler, Maxwell): Требуют **устаревшие драйверы**. Их пакеты не входят в официальные репозитории Arch Linux. Их нужно искать в AUR (например, `nvidia-390xx`, `nvidia-470xx`).

#
#
####################################################
## ✅ ПОДГОТОВКА ЗАВЕРШЕНА. ВЫБЕРИТЕ ВАРИАНТ      ##
####################################################
#



######################################################
## 📦 БЛОК 1: ВЫБОР ВАРИАНТА УСТАНОВКИ ДРАЙВЕРОВ    ##
######################################################
#
# ℹ️ Зачем: Установить драйверы, соответствующие вашей видеокарте.
# ❗ Важно: Раскомментируйте ТОЛЬКО ОДИН вариант ниже!
# 💡 Все команды закомментированы. Уберите '#' перед нужными строками.

## === ВАРИАНТ A: Intel Integrated Graphics ===##
pacman -S --noconfirm mesa vulkan-intel lib32-mesa lib32-vulkan-intel

## === ВАРИАНТ B: AMD Radeon Graphics или Open-Source NVIDIA (nouveau) ===##
pacman -S --noconfirm mesa vulkan-radeon lib32-mesa lib32-vulkan-radeon
# *(Примечание: Этот же набор пакетов обеспечивает работу с открытым драйвером nouveau для GPU NVIDIA.)*

## === ВАРИАНТ C: NVIDIA (Проприетарные драйверы) ===##
# 📌 ВАЖНО: Этот раздел делится на два подраздела: современные и устаревшие драйверы.

# --- C1. Современные GPU (Pascal, Turing, Ampere, Ada Lovelace и новее): ---

# 💡 ОФИЦИАЛЬНАЯ РЕКОМЕНДАЦИЯ NVIDIA (https://www.nvidia.com/drivers/  ):
# «Многие дистрибутивы Linux предоставляют собственные пакеты драйвера NVIDIA
#  для Linux в формате родной системы управления пакетами дистрибутива.
#  Такой подход может лучше взаимодействовать с остальной частью
#  программной среды вашего дистрибутива, и вам, вероятно, стоит использовать
#  именно его, а не официальный пакет от NVIDIA.»

# 📌 ВЕТКИ ДРАЙВЕРОВ:
# 1. **Production Branch / Studio Drivers**
#    - Рекомендовано для БОЛЬШИНСТВА ПОЛЬЗОВАТЕЛЕЙ.
#    - Обеспечивает оптимальную стабильность, производительность и
#      сертификацию ISV (независимых разработчиков ПО).
#    - Длинный жизненный цикл и регулярные обновления безопасности.
#    - Используйте ЭТУ ВЕТКУ, если вы не уверены в выборе.
#
# 2. **New Feature Branch (NFB) / Game Ready Drivers**
#    - Для тех, кто хочет получить НОВЫЕ ФУНКЦИИ, исправления ошибок
#      и поддержку новых ОС между релизами Production Branch.
#    - Подходит для геймеров и ранних adopters.
#    - Поддержка короче, чем у Production Branch.

# A. Для стандартного ядра (linux) — Production Branch:
pacman -S --noconfirm nvidia-open nvidia-utils nvidia-settings lib32-nvidia-utils opencl-nvidia lib32-opencl-nvidia

# B. Для LTS-ядра (linux-lts) — Production Branch:
pacman -S --noconfirm nvidia-open-lts nvidia-utils nvidia-settings lib32-nvidia-utils opencl-nvidia lib32-opencl-nvidia

# C. Универсальный DKMS-драйвер (для кастомных ядер) — Production Branch:
pacman -S --noconfirm nvidia-open-dkms nvidia-utils nvidia-settings lib32-nvidia-utils opencl-nvidia lib32-opencl-nvidia

# --- C2. Устаревшие GPU (Tesla, Fermi, Kepler, Maxwell): ---

# *   Эти драйверы НЕ находятся в официальных репозиториях Arch.
# *   Их нужно искать и устанавливать из AUR. Примеры: `nvidia-390xx`, `nvidia-470xx`.
# *   Используйте `yay` или другой AUR helper.
yay -S nvidia-390xx nvidia-390xx-utils nvidia-390xx-settings lib32-nvidia-390xx-utils opencl-nvidia-390xx lib32-opencl-nvidia-390xx# Пример команды для AUR
# *(Примечание: конкретный пакет зависит от вашей архитектуры, например, `nvidia-390xx` для Fermi/Kepler, `nvidia-470xx` для Maxwell.)*

# 📌 ДЛЯ ГИБРИДНОЙ ГРАФИКИ (Intel/AMD + NVIDIA):
pacman -S --noconfirm switcheroo-control
systemctl enable switcheroo-control.service

# 📌 ЕСЛИ NVIDIA — ЕДИНСТВЕННАЯ ВИДЕОКАРТА (ТОЛЬКО ДЛЯ СОВРЕМЕННЫХ GPU):
# Установка libva-nvidia-driver рекомендуется ТОЛЬКО в этом случае.
# Не устанавливайте её в системах с гибридной графикой!
pacman -S --noconfirm libva-nvidia-driver


##########################################################
## 🧰 БЛОК 2: ДОПОЛНИТЕЛЬНЫЕ ГРАФИЧЕСКИЕ БИБЛИОТЕКИ##   ##
##########################################################
#
# ℹ️ Зачем: Обеспечить совместимость с Vulkan и VA-API.
# 💡 Обязательно для всех типов GPU.

# Vulkan ICD Loader (для всех):
pacman -S --noconfirm vulkan-icd-loader lib32-vulkan-icd-loader


#################################################
## 🔧 БЛОК 3: КОНФИГУРАЦИЯ МОДУЛЕЙ И INITRAMFS ##
#################################################
#
# ℹ️ Зачем: Включить режим модсета для NVIDIA (требуется для Wayland).
# ❗ Важно: Выполняется только при установке NVIDIA драйверов (любой ветки, включая устаревшие).
# 💡 Этот блок применяется ТОЛЬКО если вы выбрали ВАРИАНТ C.

# === ТОЛЬКО ДЛЯ NVIDIA (ПРОПРИЕТАРНЫЕ): Настройка модулей и initramfs ===

# Создание файла настройки модуля NVIDIA:
'options nvidia-drm modeset=1' > /etc/modprobe.d/nvidia.conf
'options nvidia NVreg_DynamicPowerManagement=0x02' >> /etc/modprobe.d/nvidia.conf

# Пересборка initramfs (обязательно!):
mkinitcpio -P


########################################################
## 🔋 БЛОК 4: ОПЦИОНАЛЬНО — НАСТРОЙКИ ДЛЯ НОУТБУКОВ   ##
########################################################
#
# ℹ️ Зачем: Улучшить энергоэффективность на ноутбуках.
# 💡 Используется TLP — современный инструмент управления питанием.
# ❗ ВАЖНО: Если у вас гибридная графика с NVIDIA, прочитайте инструкции по настройке TLP.

## === ТОЛЬКО ДЛЯ НОУТБУКОВ (БЕЗ ГИБРИДНОЙ ГРАФИКИ NVIDIA) ===
pacman -S --noconfirm tlp tlp-rdw tlpui
systemctl enable tlp.service
systemctl mask systemd-rfkill.service systemd-rfkill.socket

## === ТОЛЬКО ДЛЯ НОУТБУКОВ (С ГИБРИДНОЙ ГРАФИКОЙ NVIDIA) ===
pacman -S --noconfirm tlp tlp-rdw tlpui
## --- Настройка TLP для совместимости с NVIDIA ---
## 1. Определите PCI-адрес NVIDIA-видеокарты:
lspci -nn | grep -i nvidia
## (Пример вывода: 01:00.0 3D controller: NVIDIA Corporation TU104 [GeForce RTX 2080 Rev. A] [10de:1e82] (rev a1))
## (PCI ID: 10de:1e82, Адрес: 01:00.0)
## 2. Отредактируйте конфигурационный файл TLP:
nano /etc/tlp.conf
## Найдите строку RUNTIME_PM_DENYLIST= и добавьте туда **адрес** вашей NVIDIA-видеокарты (в примере это `01:00.0`). Также часто добавляют аудиоконтроллер, связанный с dGPU (например, `01:00.1`).
## RUNTIME_PM_DENYLIST=#01:00.0 01:00.1#
## Сохраните файл и закройте редактор.
## 3. Включите службу TLP:
systemctl enable tlp.service
systemctl mask systemd-rfkill.service systemd-rfkill.socket


#####################################################
## 🧪 БЛОК 6: ПРОВЕРКА РАБОТЫ ПОСЛЕ ПЕРЕЗАГРУЗКИ   ##
#####################################################
#
# ℹ️ Зачем: Убедиться, что драйверы работают корректно.
# ❗ Важно: Выполняется ТОЛЬКО после первой загрузки в новую систему.
# 💡 Выполняйте от имени обычного пользователя.

# Установите диагностические утилиты (если ещё не установлены):
# pacman -S --noconfirm mesa-utils vulkan-tools glmark2 mesa-demos libva-utils

# === Проверки: ===

# NVIDIA (проприетарные):
nvidia-smi

# Vulkan (все GPU):
vulkaninfo --summary

# VA-API (если установлен libva-nvidia-driver или nouveau):
vainfo

# Переключение GPU (гибридная графика):
switcherooctl list
switcherooctl launch --gpu 1 glxinfo | grep 'OpenGL renderer string'

# === Визуальные тесты: ===

# Базовая анимация
glxgears

# Vulkan-куб
vkcube

# OpenGL бенчмарк
# glmark2

# На дискретной карте
switcherooctl launch --gpu 1 glmark2
switcherooctl launch --gpu 1 vkcube
#
#############################################
## ✅ РУКОВОДСТВО ПО ДРАЙВЕРАМ ЗАВЕРШЕНО   ##
