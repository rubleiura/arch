# ##########################################################################
# 🔧 ПЕРЕРАБОТАННАЯ ИНСТРУКЦИЯ ПО НАСТРОЙКЕ UFW НА ARCH LINUX
# ##########################################################################
# ⚠️ ВНИМАНИЕ: Выполняйте команды по порядку в терминале.
#              Не запускайте как единый скрипт!
#              Для удаленного сервера: ОБЯЗАТЕЛЬНО настройте SSH до 'sudo ufw enable'!
# ##########################################################################

# ################################################################
# 📦 ЧАСТЬ 1: УСТАНОВКА, ПОДГОТОВКА И ОСНОВЫ
# ################################################################

# 1.1 Установка UFW (Uncomplicated Firewall)
# 🛡️ UFW — это простой интерфейс для управления `iptables`.
sudo pacman -S --noconfirm ufw gufw ufw-extras

# 1.2 Проверка текущего статуса фаервола
# ℹ️ Это покажет, активен ли UFW и какие правила установлены (если есть).
sudo ufw status

# 1.3 (Опционально) Отключение конфликтующих системных брандмауэров
# ⚠️ Если вы используете `iptables` или `nftables` напрямую, UFW может с ними конфликтовать.
#    Эти команды отключат их, если они активны. Используйте с осторожностью!
sudo systemctl stop iptables 2>/dev/null; sudo systemctl disable iptables 2>/dev/null
sudo systemctl stop nftables 2>/dev/null; sudo systemctl disable nftables 2>/dev/null

# ################################################################
# 🔐 ЧАСТЬ 2: КРИТИЧЕСКИ ВАЖНО - НАСТРОЙКА ПОЛИТИК И SSH
# ################################################################

# 2.1 Установка базовых политик по умолчанию (Принцип наименьших привилегий)
# 🛡️ 'deny incoming': Всем входящим соединениям, не разрешенным явно, будет отказано.
# 🟢 'allow outgoing': Все исходящие соединения разрешены.
#    Это безопасная отправная точка.
sudo ufw default deny incoming
sudo ufw default allow outgoing

# 2.2 ⚠️ НАСТРОЙКА SSH - КРИТИЧЕСКИЙ ШАГ ПЕРЕД ВКЛЮЧЕНИЕМ UFW ⚠️
# 🔑 Если вы управляете сервером по SSH, вы ДОЛЖНЫ разрешить SSH-доступ до включения UFW,
#    иначе вы потеряете доступ к системе!
# ⚠️ ВНИМАНИЕ: Выберите и выполните ОДИН из следующих вариантов настройки SSH!

# 🟢 Вариант A1: Разрешить SSH на стандартном порту (22/tcp) для всех.
sudo ufw allow 22/tcp

# 🔵 Вариант A2: Разрешить SSH на нестандартном порту (например, 2222).
sudo ufw allow 2222/tcp

# 🔵 Вариант A3: Разрешить SSH только с определенного IP-адреса (рекомендуется для безопасности).
sudo ufw allow from 192.168.1.100 to any port 22 proto tcp

# 🔵 Вариант A4: Защита SSH от брутфорс-атак (ограничение количества попыток).
# sudo ufw limit 22/tcp

# 🟢 Вариант A5: Комбинированный подход (например, нестандартный порт + лимит). Рекомендуется.
sudo ufw allow 2222/tcp
sudo ufw limit 2222/tcp

# ################################################################
# 🌐 ЧАСТЬ 3: НАСТРОЙКА ПРАВИЛ ДЛЯ ДРУГИХ СЛУЖБ
# ################################################################

# 3.1 Стандартные порты для веб-сервера
sudo ufw allow 80/tcp    # 🌍 HTTP
sudo ufw allow 443/tcp   # 🔐 HTTPS

# 3.2 Другие распространенные службы (примеры)
sudo ufw allow 53/tcp    # 🌐 DNS (TCP)
sudo ufw allow 53/udp    # 🌐 DNS (UDP)
sudo ufw allow 3306/tcp  # 🗄️ MySQL
sudo ufw allow 5432/tcp  # 🗄️ PostgreSQL

# 3.3 Разрешение диапазона портов (например, для игр или медиа-серверов)
sudo ufw allow 6500:6800/udp

# 3.4 Разрешение трафика для всей подсети (например, доступ к SSH из внутренней сети)
sudo ufw allow from 10.0.0.0/24 to any port 22 proto tcp

# 3.5 (Продвинуто) Применение правила к конкретному интерфейсу
# 🖧 Полезно на серверах с несколькими сетями.
sudo ufw allow in on eth0 to any port 80

# ################################################################
# ✅ ЧАСТЬ 4: ВКЛЮЧЕНИЕ И ПРОВЕРКА UFW
# ################################################################

# 4.1 Проверка всех настроенных правил перед включением
# ⚠️ ВНИМАНИЕ: Убедитесь, что ваше SSH-правило есть в списке!
sudo ufw status verbose

# 4.2 Включение фаервола
# ⚡ Это активирует все настроенные правила.
# ⚠️ Убедитесь, что вы можете подключиться по SSH после выполнения этой команды!
sudo ufw enable

# 4.3 Включение службы UFW для автозапуска при загрузке
# 🔄 Это гарантирует, что UFW будет активен после перезагрузки системы.
sudo systemctl enable ufw
sudo systemctl start ufw

# 4.4 Финальная проверка статуса
sudo ufw status
sudo ufw status numbered

# ################################################################
# 📝 ЧАСТЬ 5: ЛОГИРОВАНИЕ И ПРОДВИНУТЫЕ ФУНКЦИИ
# ################################################################

# 5.1 Включение логирования (рекомендуется)
# 📊 Это поможет в диагностике проблем и мониторинге.
sudo ufw logging on

# 5.2 Уровни логирования (опционально)
sudo ufw logging low|medium|high|full

# 5.3 Проверка логов (если включено)
# 📁 Логи обычно находятся в /var/log/ufw.log
sudo tail -f /var/log/ufw.log

# 5.4 (Опционально) Включение поддержки IPv6
# 🌐 Редактируем файл конфигурации UFW
sudo nano /etc/default/ufw
# Убедитесь, что строка IPV6=yes
# Затем выполните
sudo ufw disable && sudo ufw enable

# ################################################################
# 🛠️ ЧАСТЬ 6: ОСНОВНЫЕ КОМАНДЫ УПРАВЛЕНИЯ
# ################################################################

# 6.1 Просмотр всех правил с номерами (для удаления)
sudo ufw status numbered

# 6.2 Удаление правила по номеру (например, удалить правило под номером 2)
sudo ufw delete 2

# 6.3 Удаление правила по тексту (например, удалить правило для 80/tcp)
sudo ufw delete allow 80/tcp

# 6.4 Временное отключение всех правил UFW
sudo ufw disable

# 6.5 Перезагрузка правил
sudo ufw reload

# 6.6 Полный сброс UFW (удаляет все правила и настройки!)
# 🚨 Используйте ТОЛЬКО в крайнем случае!
sudo ufw reset

# ################################################################
# 🚨 ЧАСТЬ 7: АВАРИЙНЫЕ СЦЕНАРИИ
# ################################################################

# 🔧 Если вы заблокировали себя и потеряли доступ по SSH:
# 1. Если у вас физический доступ к серверу:
sudo ufw disable
#    # Затем снова настройте SSH и включите UFW.
sudo ufw allow 2222/tcp
# или ваш порт
sudo ufw enable
# 2. Если у вас есть доступ к консоли через провайдера (например, VNC/IPMI):
#    Повторите действия из п.1 через консоль.

